    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed

        Date CurrentDate = new Date();
        if (LoginCheck() && WrongPasswordTimes < 3 && CurrentDate.after(user.getBanDate())) {
            for (User user_check : Main.users) {
                if (user_check.getUsername().equals(Username)) {
                    user = user_check;
                    break;
                }
            }
            if (user.getRank().equals(UserRank.Administrator)) {
                // logging in
                this.setContentPane(new AdminMenuJPanel());
            } else {
                this.setContentPane(new UserMenuJPanel());
            }
            pack();
        } else if (CurrentDate.before(user.getBanDate())) {
            JOptionPane.showMessageDialog(this,"User is banned!","Error",0);
        } else if (WrongPasswordTimes >= 3) {
            user.setBanDate(new Date(System.currentTimeMillis() + 3600000));
            JOptionPane.showMessageDialog(this,"User has been Banned!!","Error",0);
        }

    }//GEN-LAST:event_LoginButtonActionPerformed

