    private void updatelist(int i) {

        switch (i) {
            case 0:
                ArrayList<Asset> NewAssetList0 = new ArrayList();
                for (Asset a : Main.assets) {
                    if (a.getModel().contains(SearchjTextField0.getText())) {
                        NewAssetList0.add(a);
                    }
                }
                jList0.setListData(NewAssetList0.toArray());
                break;
            case 1:
                ArrayList<Report> NewReportList1 = new ArrayList();
                for (Report r : Main.reports) {
                    if (r.getUserID().startsWith(SearchjTextField1.getText())) {
                        NewReportList1.add(r);
                    }
                }
                jList1.setListData(NewReportList1.toArray());
                break;
            case 2:
                ArrayList<User> NewUserList = new ArrayList();
                for (User u : Main.users) {
                    if (u.toString().startsWith(SearchjTextField2.getText())) {
                        NewUserList.add(u);
                    }
                }
                jList2.setListData(NewUserList.toArray());
                break;
            case 3:
                ArrayList<Object> NewReportList3 = new ArrayList();
                for (Report r : Main.reports) {
                    if (r.getAssetID().startsWith(SearchjTextField3.getText())) {
                        NewReportList3.add(r);
                    }
                }
                jList3.setListData(Reverse(NewReportList3).toArray());
                break;
            case 4:
                ArrayList<Report> userreport_al = new ArrayList();
                for (Report r : Main.reports) {
                    if (r.getUserID().equals(Main.login.getUser().toString())) {
                        userreport_al.add(r);
                    }
                }
                userreports_arr = new Report[userreport_al.size()];
                userreports_arr = userreport_al.toArray(userreports_arr);
                break;
            case 5:
                ArrayList<Report> NewReportList5 = new ArrayList();
                for (Report r : Main.reports) {
                    if (r.getCause().contains(SearchjTextField5.getText())) {
                        NewReportList5.add(r);
                    }
                }
                jList5.setListData(NewReportList5.toArray());
                break;
            case 6:
                ArrayList<Asset> NewAssetList6 = new ArrayList();
                for (Asset a : Main.assets) {
                    if (a.getLocation()[1].contains(SearchjTextField6.getText())) {
                        NewAssetList6.add(a);
                    }
                }
                jList6.setListData(NewAssetList6.toArray());
                break;
        }

    }

    private ArrayList Reverse(ArrayList List) {

        ArrayList<Object> ReversedList = new ArrayList();
        for (int i = List.size(); i > 0; i--) {
            ReversedList.add(List.get(i - 1));
        }
        return ReversedList;
    }


