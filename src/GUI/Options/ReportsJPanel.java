    private void jList0ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList0ValueChanged

        if (evt.getValueIsAdjusting()) {//This line prevents double events
            asset = (Asset) jList0.getSelectedValue();
            // setting previous information
            ShownIDjLabel0.setText(asset.toString());
            ShownModeljLabel0.setText(asset.getModel());
            ShownBDatejLabel0.setText(asset.getBDate());
            ShownLocationjLabel0.setText(asset.getLocation()[0] + " , "
                    + asset.getLocation()[1] + " , " + asset.getLocation()[2]);
            ShownStatejLabel0.setText(asset.getState().toString());

            switch (asset.getType()) { // swaping panels by typ of asset
                case 0:
                    // changing panels
                    ComputerjPanel.setVisible(true);
                    SwitchjPanel0.setVisible(false);
                    PrinterjPanel0.setVisible(false);
                    // insert other values
                    Computer pc = (Computer) asset;
                    ShownCPUjLabel0.setText(pc.getCPU());
                    ShownRAMjLabel.setText(pc.getRAM());
                    ShownHardjLabel0.setText(pc.getHard());
                    ShownGraphicjLabel0.setText(pc.getGraphic());
                    break;
                case 1:
                    // changing panels
                    ComputerjPanel.setVisible(false);
                    SwitchjPanel0.setVisible(true);
                    PrinterjPanel0.setVisible(false);
                    // insert other values
                    Switch s = (Switch) asset;
                    ShownPortNumberjLabel0.setText(Integer.toString(s.getPortNumber()));
                    if (s.getManageable()) {
                        ShownManageablejLabel0.setText("Yes");
                    } else {
                        ShownManageablejLabel0.setText("No");
                    }
                    break;
                case 2:
                    // changing panels
                    ComputerjPanel.setVisible(false);
                    SwitchjPanel0.setVisible(false);
                    PrinterjPanel0.setVisible(true);
                    // insert other values
                    Printer p = (Printer) asset;
                    if (p.getColorPrint()) {
                        ShownColorPrintjLabel0.setText("Yes");
                    } else {
                        ShownColorPrintjLabel0.setText("No");
                    }
                    break;
            }
        }

    }//GEN-LAST:event_jList0ValueChanged

