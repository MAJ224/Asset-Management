    private void ready() throws IllegalStateException {

        if (model.equals("") || location[0].equals("") || location[1].equals("")
                || location[2].equals("")) {
            throw new IllegalStateException("One or some of asset fields are invalid!");
        }
        if (AssetsjComboBox.getSelectedIndex() == 0 && (cpu.equals("") || ram.equals("")
                || hard.equals("") || graphic.equals(""))) {
            throw new IllegalStateException("One or some of computer Fields are invalid!");
        }
        if (AssetsjComboBox.getSelectedIndex() == 1 && portnumber <= 1) {
            throw new IllegalStateException("Port Number must be higher than 1!");
        }        
        date = new Date().toGMTString();

    }

