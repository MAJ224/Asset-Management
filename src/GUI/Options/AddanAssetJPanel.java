    private void AssetsjComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AssetsjComboBoxActionPerformed

        // switch between panels
        switch (AssetsjComboBox.getSelectedIndex()) {
            case 0:
                AddPCjPanel.setVisible(true);
                AddPrinterjPanel.setVisible(false);
                AddSwitchjPanel.setVisible(false);
                break;
            case 1:
                AddPCjPanel.setVisible(false);
                AddSwitchjPanel.setVisible(true);
                AddPrinterjPanel.setVisible(false);
                break;
            case 2:
                AddPCjPanel.setVisible(false);
                AddSwitchjPanel.setVisible(false);
                AddPrinterjPanel.setVisible(true);
                break;
        }

    }//GEN-LAST:event_AssetsjComboBoxActionPerformed

