    private void jListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListValueChanged

        if (evt.getValueIsAdjusting()) {//This line prevents double events
            asset = (Asset) jList.getSelectedValue();
            // setting previous information
            ShownIDjLabel.setText(asset.toString());
            ShownModeljLabel.setText(asset.getModel());
            ShownStatejLabel.setText(asset.getState().toString());
        }

    }//GEN-LAST:event_jListValueChanged

    private void SubmitRepairReportjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitRepairReportjButtonActionPerformed
        
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");         
        Main.reports.add(new Report(asset.toString(), asset.getModel(), user.toString()
                , user.getUsername(), cause, repair, formatter.format(new Date())));
        Main.BacktoMainMenu();
        
    }//GEN-LAST:event_SubmitRepairReportjButtonActionPerformed

    void updatelist() {
        
        ArrayList <Asset> NewList = new ArrayList();
        for (Asset a : Main.assets){
            if (a.toString().startsWith(SearchjTextField.getText())){
                NewList.add(a);
            }           
        }
        jList.setListData(NewList.toArray());
        
    }

